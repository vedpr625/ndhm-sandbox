
package in.vedprakash.sandbox.demo.consentrequests.oninit;


public class ConsentRequesOnInit {

    private String requestId;
    private String timestamp;
    private ConsentRequest consentRequest;
    private Resp resp;

    public String getRequestId() {
        return requestId;
    }

    public void setRequestId(String requestId) {
        this.requestId = requestId;
    }

    public String getTimestamp() {
        return timestamp;
    }

    public void setTimestamp(String timestamp) {
        this.timestamp = timestamp;
    }

    public ConsentRequest getConsentRequest() {
        return consentRequest;
    }

    public void setConsentRequest(ConsentRequest consentRequest) {
        this.consentRequest = consentRequest;
    }

    public Resp getResp() {
        return resp;
    }

    public void setResp(Resp resp) {
        this.resp = resp;
    }

}
